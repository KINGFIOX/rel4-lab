use super::{MMIO_FRAME_SIZE, PciHost, UartMmio, VirtioTransport};

pub const UART0_MMIO_BASE: u64 = 0x1fe0_01e0;
pub const UART0_MMIO_SIZE: u64 = 0x100;
pub const UART0_MMIO_FRAME_BASE: u64 = 0x1fe0_0000;
pub const XV6_UART_MMIO_FRAME_VADDR: u64 = 0x5000_4000;
pub const XV6_UART_MMIO_VADDR: u64 =
    XV6_UART_MMIO_FRAME_VADDR + (UART0_MMIO_BASE - UART0_MMIO_FRAME_BASE);
pub const UART0_IRQ: u64 = 2;

pub const XV6_DEVICE_MMIO_BASE: u64 = UART0_MMIO_FRAME_BASE;
pub const XV6_DEVICE_MMIO_SIZE: u64 = MMIO_FRAME_SIZE;

pub const LOONGARCH64_PCIE_ECAM_BASE: u64 = 0x2000_0000;
pub const LOONGARCH64_PCIE_ECAM_SIZE: u64 = 0x0800_0000;
pub const LOONGARCH64_PCIE_IO_BASE: u64 = 0x1800_4000;
pub const LOONGARCH64_PCIE_IO_SIZE: u64 = 0x0000_c000;
pub const LOONGARCH64_PCIE_MEM_BASE: u64 = 0x4000_0000;
pub const LOONGARCH64_PCIE_MEM_SIZE: u64 = 0x4000_0000;
pub const LOONGARCH64_PCIE_LEGACY_IRQ_BASE: u64 = 16;
pub const LOONGARCH64_PCIE_LEGACY_IRQ_COUNT: u64 = 4;
pub const LOONGARCH64_PCH_MSI_BASE: u64 = 0x2ff0_0000;
pub const LOONGARCH64_PCH_MSI_SIZE: u64 = 0x8;
pub const LOONGARCH64_PCH_MSI_BASE_VECTOR: u64 = 0x20;
pub const LOONGARCH64_PCH_MSI_NUM_VECTORS: u64 = 0xe0;

pub const XV6_PCIE_ECAM_VADDR: u64 = 0x5001_0000;
pub const XV6_PCIE_ECAM_MAP_SIZE: u64 = 0x0010_0000;
pub const XV6_PCIE_MEM_VADDR: u64 = 0x5011_0000;
pub const XV6_PCIE_MEM_MAP_SIZE: u64 = 0x0001_0000;
pub const XV6_PCIE_IO_VADDR: u64 = 0x5012_0000;
pub const XV6_PCIE_IO_MAP_SIZE: u64 = LOONGARCH64_PCIE_IO_SIZE;
pub const XV6_PCIE_MSI_VADDR: u64 = 0x5013_0000;
pub const XV6_PCIE_MSI_MAP_SIZE: u64 = MMIO_FRAME_SIZE;

pub const UART0: UartMmio = UartMmio {
    paddr: UART0_MMIO_BASE,
    size: UART0_MMIO_SIZE,
    frame_paddr: UART0_MMIO_FRAME_BASE,
    frame_vaddr: XV6_UART_MMIO_FRAME_VADDR,
    vaddr: XV6_UART_MMIO_VADDR,
    irq: UART0_IRQ,
};

pub const LOONGARCH64_PCIE_HOST: PciHost = PciHost {
    ecam_paddr: LOONGARCH64_PCIE_ECAM_BASE,
    ecam_size: LOONGARCH64_PCIE_ECAM_SIZE,
    io_paddr: LOONGARCH64_PCIE_IO_BASE,
    io_size: LOONGARCH64_PCIE_IO_SIZE,
    mem_paddr: LOONGARCH64_PCIE_MEM_BASE,
    mem_size: LOONGARCH64_PCIE_MEM_SIZE,
    legacy_irq_base: LOONGARCH64_PCIE_LEGACY_IRQ_BASE,
    legacy_irq_count: LOONGARCH64_PCIE_LEGACY_IRQ_COUNT,
    msi_paddr: LOONGARCH64_PCH_MSI_BASE,
    msi_size: LOONGARCH64_PCH_MSI_SIZE,
    msi_base_vector: LOONGARCH64_PCH_MSI_BASE_VECTOR,
    msi_num_vectors: LOONGARCH64_PCH_MSI_NUM_VECTORS,
};

pub const VIRTIO_TRANSPORT: VirtioTransport = VirtioTransport::Pci(LOONGARCH64_PCIE_HOST);
